# Write a program that writes to a file ICAO.txt the contents of a dictionary containing ICAO spelling alphabet. 

letters = {
    "A": "Alfa",
    "B": "Bravo",
    "C": "Charlie",
    "D": "Delta",
    "E": "Echo",
    "F": "Foxtrot",
    "G": "Golf",
    "H": "Hotel",
    "I": "India",
    "J": "Juliett",
    "K": "Kilo",
    "L": "Lima",
    "M": "Mike",
    "N": "November",
    "O": "Oscar",
    "P": "Papa",
    "Q": "Quebec",
    "R": "Romeo",
    "S": "Sierra",
    "T": "Tango",
    "U": "Uniform"
}

with open("ICAO.txt", "w") as file:
    for k, v in letters.items():
        file.write(k + " " + v + "\n")

